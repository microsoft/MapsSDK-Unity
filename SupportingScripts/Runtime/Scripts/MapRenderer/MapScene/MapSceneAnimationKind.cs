namespace Microsoft.Maps.Unity
{
    /// <summary>
    /// Specifies the animation to use when setting a MapScene.
    /// </summary>
    public enum  MapSceneAnimationKind
    {
        /// <summary>
        /// No animation.
        /// </summary>
        None,

        /// <summary>
        /// A linear animation.
        /// </summary>
        Linear,

        /// <summary>
        /// A parabolic animation.
        /// </summary>
        Bow
    }
}
